import os

import google.generativeai as genai

genai.configure(api_key=os.getenv('GEMINI_API_KEY'))

# Create the model
# See https://ai.google.dev/api/python/google/generativeai/GenerativeModel
generation_config = {
    "temperature": 1,
    "top_p": 0.95,
    "top_k": 64,
    "max_output_tokens": 8192,
    "response_mime_type": "text/plain",
}
safety_settings = [
    {
        "category": "HARM_CATEGORY_HARASSMENT",
        "threshold": "BLOCK_MEDIUM_AND_ABOVE",
    },
    {
        "category": "HARM_CATEGORY_HATE_SPEECH",
        "threshold": "BLOCK_MEDIUM_AND_ABOVE",
    },
    {
        "category": "HARM_CATEGORY_SEXUALLY_EXPLICIT",
        "threshold": "BLOCK_MEDIUM_AND_ABOVE",
    },
    {
        "category": "HARM_CATEGORY_DANGEROUS_CONTENT",
        "threshold": "BLOCK_MEDIUM_AND_ABOVE",
    },
]

model = genai.GenerativeModel(
    model_name="gemini-1.5-flash-latest",
    safety_settings=safety_settings,
    generation_config=generation_config,
)


def generate_text(message):
    chat_session = model.start_chat(history=[])

    response = chat_session.send_message(message)

    return response.text


def sentiment_analysis(message):
    prepared_string = f"What is the sentiment of the following sentence, which is delimited with triple backticks? Just give the sentiment in one word lowercase if it is positive, negative or neutral in Review text: ```{message}```"

    chat_session = model.start_chat(history=[])

    response = chat_session.send_message(prepared_string)

    return response.text.strip().lower()